    val colors = listOf(
        Color(0xFF4CAF50), // ÁªøËâ≤
        Color(0xFF2196F3), // ËìùËâ≤
        Color(0xFFFF9800), // Ê©ôËâ≤
        Color(0xFF9C27B0), // Á¥´Ëâ≤
        Color(0xFF00BCD4), // ÈùíËâ≤
        Color(0xFFF44336), // Á∫¢Ëâ≤
        Color(0xFF795548), // Ê£ïËâ≤
        Color(0xFF607D8B), // ËìùÁÅ∞Ëâ≤
        Color(0xFFE91E63), // Á≤âËâ≤
        Color(0xFF8BC34A)  // ÊµÖÁªøËâ≤
